///     - "1.2.3"   包含 "1.2.3.4"
///     - "1.2.3"   包含 "1.2.3.15"
///     - "1.2.3" 不包含 "1.3"
///     - "1.2.3" 不包含 "2.3"
///     - "1.2.3\~4.5.6" 不包含 "0"
///     - "1.2.3\~4.5.6" 不包含 "1.0"
///     - "1.2.3\~4.5.6" 不包含 "1.0.0"
///     - "1.2.3\~4.5.6" 不包含 "1.2"
