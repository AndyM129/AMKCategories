///
/// - 特别说明
///     - 区间判断，是「前闭后开」，例如 `v1~v2` 意为 `[v1, v2)`，即 `v1 ≤ 生效版本 < v2`
///     - 版本号的精度 会影响区间判断，例如「`1.2.3~4.5.6` 不包含 `1.2`，包含 `1.2.3`，不包含`4.5.6`」
